export const TOTAL_PLAYERS = 100000;

// Country distribution map
type CEntry = {
  name: string;
  flag: string;
  confederation: Player["confederation"];
  start: number;
  count: number;
};
let _distribution: CEntry[] | null = null;

function buildDistribution(): CEntry[] {
  if (_distribution) return _distribution;
  const allCountries = _allCountries;
  const weights: Record<string, number> = {
    Brazil: 8000,
    Germany: 7500,
    Spain: 7000,
    France: 6500,
    England: 6000,
    Argentina: 6000,
    Italy: 5500,
    Portugal: 4000,
    Netherlands: 3500,
    Belgium: 3000,
    Mexico: 3000,
    Nigeria: 2500,
    "South Korea": 2500,
    Japan: 2500,
    USA: 2500,
    Russia: 2000,
    Poland: 2000,
    Turkey: 2000,
    Ghana: 1800,
    Egypt: 1800,
    Colombia: 1800,
    Uruguay: 1500,
    Chile: 1500,
    Senegal: 1500,
    Morocco: 1500,
    Iran: 1500,
    Algeria: 1500,
    Australia: 1200,
    Croatia: 1200,
    Serbia: 1200,
    Ukraine: 1200,
    Sweden: 1200,
    Denmark: 1000,
    Switzerland: 1000,
    Austria: 1000,
    Greece: 1000,
    India: 800,
  };
  const totalWeight = allCountries.reduce(
    (s, c) => s + (weights[c.name] || 200),
    0,
  );
  let cursor = 0;
  _distribution = allCountries.map((c) => {
    const w = weights[c.name] || 200;
    const count = Math.round((w / totalWeight) * TOTAL_PLAYERS);
    const entry: CEntry = {
      name: c.name,
      flag: c.flag,
      confederation: c.confederation as Player["confederation"],
      start: cursor,
      count,
    };
    cursor += count;
    return entry;
  });
  return _distribution;
}

function makePlayer(
  country: CEntry,
  localIdx: number,
  globalId: number,
): Player {
  const conf = country.confederation;
  const fns = firstNamesByRegion[conf] || firstNamesByRegion.UEFA;
  const lns = lastNamesByRegion[conf] || lastNamesByRegion.UEFA;
  const clubs = clubsByConf[conf] || clubsByConf.UEFA;
  const seed1 = globalId * 7 + localIdx * 3;
  const seed2 = globalId * 11 + localIdx * 5;
  const name = `${fns[seed1 % fns.length]} ${lns[seed2 % lns.length]}`;
  const pos = positions[(globalId * 3 + localIdx) % positions.length];
  const footSeed = (globalId + localIdx * 2) % 10;
  const bestFoot: Player["bestFoot"] =
    footSeed < 6 ? "Right" : footSeed < 9 ? "Left" : "Both";
  const goals =
    pos === "GK"
      ? 0
      : pos === "ST" || pos === "CF"
        ? 50 + (localIdx % 300)
        : pos === "LW" || pos === "RW" || pos === "CAM"
          ? 20 + (localIdx % 150)
          : localIdx % 40;
  const assists =
    pos === "GK"
      ? 0
      : pos === "CAM"
        ? 40 + (localIdx % 200)
        : pos === "LW" || pos === "RW"
          ? 30 + (localIdx % 150)
          : localIdx % 80;
  return {
    id: globalId,
    name,
    country: country.name,
    flag: country.flag,
    club: clubs[(seed1 + seed2) % clubs.length],
    position: pos,
    bestFoot,
    goals,
    assists,
    matchesPlayed: 50 + (localIdx % 450),
    age: 18 + (localIdx % 20),
    height: heights[(globalId * 3 + localIdx) % heights.length],
    weight: weights[(globalId * 5 + localIdx * 2) % weights.length],
    confederation: conf,
    nationality: country.name,
  };
}

export function generatePlayersPage(
  page: number,
  pageSize: number,
  search?: string,
  filterConfed?: string,
  filterPos?: string,
  filterCountry?: string,
): { players: Player[]; total: number } {
  const dist = buildDistribution();
  const hasFilter = search || filterConfed || filterPos || filterCountry;

  if (hasFilter) {
    const results: Player[] = [];
    const q = search?.toLowerCase();
    let _scanned = 0;
    for (const country of dist) {
      if (filterConfed && country.confederation !== filterConfed) {
        _scanned += country.count;
        continue;
      }
      if (filterCountry && country.name !== filterCountry) {
        _scanned += country.count;
        continue;
      }
      for (let i = 0; i < country.count && results.length < 5000; i++) {
        const p = makePlayer(country, i, country.start + i + 1);
        if (filterPos && p.position !== filterPos) continue;
        if (
          q &&
          !p.name.toLowerCase().includes(q) &&
          !p.country.toLowerCase().includes(q) &&
          !p.club.toLowerCase().includes(q)
        )
          continue;
        results.push(p);
        if (results.length >= 5000) break;
      }
      _scanned += country.count;
      if (results.length >= 5000) break;
    }
    const start = page * pageSize;
    return {
      players: results.slice(start, start + pageSize),
      total: results.length,
    };
  }

  const start = page * pageSize;
  const end = Math.min(start + pageSize, TOTAL_PLAYERS);
  const players: Player[] = [];
  let accumulated = 0;
  for (const country of dist) {
    const countryEnd = accumulated + country.count;
    if (countryEnd <= start) {
      accumulated = countryEnd;
      continue;
    }
    if (accumulated >= end) break;
    for (let i = 0; i < country.count; i++) {
      const globalId = accumulated + i;
      if (globalId < start) continue;
      if (globalId >= end) break;
      players.push(makePlayer(country, i, globalId + 1));
    }
    accumulated = countryEnd;
  }
  return { players, total: TOTAL_PLAYERS };
}

export function getPlayerCountByCountry(countryName: string): number {
  const dist = buildDistribution();
  return dist.find((c) => c.name === countryName)?.count ?? 0;
}
